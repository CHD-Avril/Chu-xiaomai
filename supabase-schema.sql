create or replace function public.is_current_user_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select auth.uid() is not null
    and exists (
      select 1
      from public.admin_users admin_user
      where lower(admin_user.email) = lower(auth.email())
    );
$$;

